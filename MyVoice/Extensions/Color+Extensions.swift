extension Color {
    static var background: Color? {
        UIColor.background?.asColor
    }

    static var blackCustom: Color? {
        UIColor.blackCustom?.asColor
    }

    static var blueLight: Color? {
        UIColor.blueLight?.asColor
    }

    static var blueDark: Color? {
        UIColor.blueDark?.asColor
    }

    static var orangeLight: Color? {
        UIColor.orangeLight?.asColor
    }

    static var orangeMain: Color? {
        UIColor.orangeMain?.asColor
    }

    static var redLight: Color? {
        UIColor.redLight?.asColor
    }

    static var redMain: Color? {
        UIColor.redMain?.asColor
    }

    static var whiteCustom: Color? {
        UIColor.whiteCustom?.asColor
    }

    static var purpleLight: Color? {
        UIColor.purpleLight?.asColor
    }

    static var purpleMain: Color? {
        UIColor.purpleMain?.asColor
    }
}
